import cv2
import numpy as np

def draw_boxes_on_image(image_path, boxes, color=(0, 255, 0), thickness=2, save_path=None, cropped=False):
    """
    在图像上绘制多个 bounding box。
    
    Args:
        image_path (str): 图像路径
        boxes (List or ndarray): [N, 4]，每行为 (x1, y1, x2, y2)
        color (tuple): 框的颜色 (B, G, R)
        thickness (int): 线条粗细
        save_path (str): 若不为 None，则保存到该路径
    """
    # 读取图像（BGR）
    image = cv2.imread(image_path)
    if image is None:
        raise FileNotFoundError(f"无法读取图像: {image_path}")

    # 画每一个框
    count = 0
    for box in boxes:
        count += 1
        x1, y1, x2, y2 = map(int, box)
        cv2.rectangle(image, (x1, y1), (x2, y2), color, thickness)
        
        # 切割图像
        if cropped:
            cropped = image[y1:y2, x1:x2]
            cv2.imwrite("cropped_" + str(count) + ".jpg", cropped)
        

    # 保存（可选）
    if save_path:
        cv2.imwrite(save_path, image)
        


